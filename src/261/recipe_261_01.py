import numpy as np

x = np.array([1, 2, 3])
y = np.array([4, 5, 6])

# 덧셈
val1 = x + y
print(val1)

# 뺄셈
val2 = x - y
print(val2)

# 곱셈(스칼라 곱)
val3 = x * 2
print(val3)

# 나눗셈(스칼리 곱)
val4 = x / 2
print(val4)

# 곱셍(아다마르 곱)
val5 = x * y
print(val5)

# 나눗셈(아다마르 곱)
val6 = x / y
print(val6)
